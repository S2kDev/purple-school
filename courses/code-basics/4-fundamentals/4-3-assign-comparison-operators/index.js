// операции с присвоением (краткая форма)
let age = 18 + 3; // 21
age += 2; // age = age + 2 = 23
age -= 3; // age = age - 3 = 20
age *= 2; // age = age * 2 = 40
age /= 2; // age = age / 2 = 20

// инкремент/декремент
age++; // age = age + 1 = 21
age--; // age = age - 1 = 20
console.log(age);

// операции сравнения (булева логика)
const personAge = 33;
console.log(personAge > age); // 33 > 20, true
console.log(personAge >= age); // true
console.log(personAge < age); // false
console.log(personAge <= age); // false
console.log(personAge == age); // false
