// операции с числами
let width = 5;
let height = 10;
let area = width * height;
console.log(area); // 50 (т.е. 5 * 10)

let division = height / width;
console.log(division); // 2

let power = 3 * 3 * 3;
let power2 = 3 ** 3;
let power3 = Math.pow(3, 3);
console.log(power3); // 27

// операции со строками
let city = 'Vitebsk';
let street = 'Mira';
console.log(city + ', ' + street + ' 21'); // Vitebsk, Mira 21
